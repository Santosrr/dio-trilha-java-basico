public class ExemploForArray {

    public static void main(String[] args) {
        // em arrays o ídice inicia em ZERO
        String alunos[] = { "FELIPE", "JONAS", "JULIA", "MARCOS" };

        for(String aluno : alunos) {
            System.out.println("O nome do aluno é:"  +  aluno);
        }
        }
    }
